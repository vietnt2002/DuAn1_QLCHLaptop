    private void btnEdit4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdit4ActionPerformed
        // TODO add your handling code here:
        try {
            if (ULHelper.checknull(txtMa4, "Không được để mã trống!")) {
                return;
            }

            if (ULHelper.checknull(txtDPG, "Không được để tên trống!")) {
                return;
            }
            lstMH = svcMH.getAll();

            Date tao = lstMH.get(index).getNgayTao();
            int stt = (radHet4.isSelected() ? 1 : 0);
            ManHinh mau = new ManHinh(txtMa4.getText(), txtDPG.getText(), Double.parseDouble(txtDPG.getText()), tao, date, stt);
            int thongBao = svcMH.sua(mau);
            if (thongBao == 1) {
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
                filltableMS();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại!");
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEdit4ActionPerformed

