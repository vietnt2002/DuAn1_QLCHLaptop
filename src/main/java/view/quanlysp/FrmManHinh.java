    public void filltableMS() {
        try {
            modelManHinh.setRowCount(0);
            lstMH = svcMH.getAll();
            for (ManHinh a : lstMH) {
                modelManHinh.addRow(new Object[]{
                    a.getMa(), a.getDoPhanGiai(), a.getInch(), a.getNgayTao(), a.getNgaySua(), a.getStatus(a.getTrangThai())
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void showdetailMS(int index) {
        try {
            txtMa4.setText(lstMH.get(index).getMa());
            txtDPG.setText(lstMH.get(index).getDoPhanGiai());
            txtInch.setText("" + lstMH.get(index).getInch());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

