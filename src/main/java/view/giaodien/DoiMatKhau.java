    private boolean kiemTra() {
        if (utilities.ULHelper.checknull(txtMatKhauCu, "Không được để trống")) {
            return false;
        }

        if (utilities.ULHelper.checknull(txtMatKhauMoi, "Không được để trống")) {
            return false;
        }
        if (utilities.ULHelper.checknull(txtNhapLai, "Không được để trống")) {
            return false;
        }
        if (!txtMatKhauCu.getText().equals(matKhauRepo.checkTrungMatKhau(utilities.UserInfo.tenTK))) {
            JOptionPane.showMessageDialog(this, "Mật khẩu cũ không đúng");
            return false;
        }
        if (!txtMatKhauMoi.getText().trim().equals(txtNhapLai.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Mật khẩu nhập lại không trùng với mật khẩu mới !");
            txtMatKhauMoi.requestFocus();
            return false;
        }
        return true;
    }

